// 開始日からスケジュール日を計算する
const DAY_OFFSETS: Record<WeekDay, number> = {
  mon: 0,
  wed: 2,
  fri: 4,
  sun: 6,
};

export function getScheduledDate(post: Post, startMonday: Date): Date {
  const weekOffset = (post.week - 1) * 7;
  const dayOffset = DAY_OFFSETS[post.day];
  const date = new Date(startMonday);
  date.setDate(date.getDate() + weekOffset + dayOffset);
  return date;
}

export function getTodaysPost(startMonday: Date): Post | null {
  const today = new Date();
  const todayStr = today.toISOString().slice(0, 10);
  return (
    POSTS.find((post) => {
      const scheduled = getScheduledDate(post, startMonday);
      return scheduled.toISOString().slice(0, 10) === todayStr;
    }) ?? null
  );
}
